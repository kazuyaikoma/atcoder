#include <bits/stdc++.h>
using namespace std;


void solve(long long A, long long B, long long C, long long K){
  long long ans;
  if (K <= A) ans = 1 * K;
  else if (K <= (A + B)) ans = 1 * A;
  else ans = 1 * A - 1 * (K - A - B);
  cout << ans << endl;
}

int main(){
    long long A;
    scanf("%lld",&A);
    long long B;
    scanf("%lld",&B);
    long long C;
    scanf("%lld",&C);
    long long K;
    scanf("%lld",&K);
    solve(A, B, C, K);
    return 0;
}
